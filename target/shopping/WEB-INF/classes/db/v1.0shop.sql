-- 活动信息
CREATE TABLE ACTIVITY(
	ID INT PRIMARY KEY AUTO_INCREMENT COMMENT'主键',
	TITLE VARCHAR(50) COMMENT'标题',
	MESSAGE VARCHAR(500) COMMENT'内容'
)ENGINE=INNODB COMMENT'活动信息';

-- 管理员账户表
CREATE TABLE ADMIN_ACCOUNT(
		ID INT PRIMARY KEY AUTO_INCREMENT COMMENT'主键',
		ADMIN_NAME VARCHAR(50) NOT NULL COMMENT'管理员的登陆名',
		`PASSWORD` VARCHAR(30) NOT NULL COMMENT'登陆密码',
		ADMIN_LEVEL INT COMMENT'管理员等级 预留'

)ENGINE=INNODB COMMENT'管理员账户表';

-- 用户账户表
CREATE TABLE USER_ACCOUNT (
   ID BIGINT PRIMARY KEY AUTO_INCREMENT COMMENT '主键 ',
   USER_NAME varchar(30) NOT NULL COMMENT '用户账户名',
   USER_PASSWORD varchar(30) NOT NULL COMMENT '登陆密码',
   PAY_PASSWORD INT(6) NOT NULL COMMENT '支付密码',
	 MONEY NUMERIC(15,3) DEFAULT 0.0 COMMENT '余额',
	 ADDRESS VARCHAR(100) COMMENT'收货地址',
   REGIST_TIME DATETIME  COMMENT '注册日期',
	 IS_BUYER TINYINT(1) COMMENT'是否卖家',
   IS_FREEZING TINYINT(1) DEFAULT 0 COMMENT '是否冻结 1--是,0--否',
   IS_LOST TINYINT(1) DEFAULT 0 COMMENT '是否注销 1--是,0--否',
   IS_DELETE TINYINT(1) DEFAULT 0 COMMENT '是否删除 1--是,0--否'
) ENGINE=INNODB COMMENT='用户账户表';


-- 用户的个人信息表
CREATE TABLE PERSON_INFO (
  ID BIGINT PRIMARY KEY AUTO_INCREMENT COMMENT '主键 ',
	USER_ACCOUNT_ID BIGINT COMMENT'用户账户表 外键',
  IDENTITY_CARD VARCHAR(20)  UNIQUE COMMENT '身份证号',
  `NAME` VARCHAR(20) NOT NULL COMMENT '姓名', 
  AGE  INT  COMMENT '年龄' ,
  SEX  TINYINT(2) DEFAULT 0 COMMENT '性别：1 -男，2 -女',
	CONSTRAINT FK_PERSON_INFO_USER_ACCOUNT FOREIGN KEY(USER_ACCOUNT_ID) REFERENCES USER_ACCOUNT(ID)
)ENGINE=INNODB COMMENT'用户的个人信息表';


